package main

import "fmt"

func main() {
	// Tipe data number
	fmt.Println("Ini angka satu = ", 1)
	fmt.Println("Ini angka dua = ", 2)
	// Tipe data Boolean
	fmt.Println("Benar = ", true)
	fmt.Println("Salah = ", false)
	// Tipe data Strings
	fmt.Println("Ini adalah tipe data strings")
	fmt.Println(len("Fajar Nugraha"))
	fmt.Println("Galantixa"[0])
	fmt.Println("Galantixa"[1])
}
